package com.allcars;
import java.lang.String;

public class Audi {
    String model;
    int yearOfProduction;
    int cost;
    int weight;
    String color;

    public Audi(String model, int yearOfProduction, int cost, int weight, String color) {
        this.model = model;
        this.yearOfProduction = yearOfProduction;
        this.cost = cost;
        this.weight = weight;
        this.color = color;
    }
}
